                                <p class="exam-content-para">
                                    <b>SBI PO</b> prelims exam is conducted every year in the month of June-July and <b>SBI PO</b> mains exam is conducted in the month of August-September. The exam has three phases. Phase one is Prelims exam in which candidates have to show their competence in three sections that is reasoning ability, quantitative aptitude and English language in one hour (20 minutes for each section). Candidates who qualify prelims exam will be called for phase two that is mains exam in which candidates have to show their proficiency in four sections in 3 hours i.e. Reasoning and computer aptitude (60 minutes), data analysis and interpretation (45 minutes), General/ Economy/ Banking Awareness (35 minutes) and English Language (40 minutes).
